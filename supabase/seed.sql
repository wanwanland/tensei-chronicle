-- ============================================================
-- 転生年表 (Tensei Chronicle) - Database Schema & Seed Data
-- ============================================================

-- eras_master: 年代マスタ
CREATE TABLE IF NOT EXISTS eras_master (
  year INTEGER PRIMARY KEY,
  era_name TEXT NOT NULL,
  avg_annual_income INTEGER NOT NULL,
  inflation_rate REAL NOT NULL,
  life_expectancy REAL NOT NULL
);

-- social_events: 社会的イベント
CREATE TABLE IF NOT EXISTS social_events (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  year INTEGER NOT NULL REFERENCES eras_master(year),
  category TEXT NOT NULL,
  title TEXT NOT NULL,
  description TEXT NOT NULL
);

-- regional_data: 地域別データ
CREATE TABLE IF NOT EXISTS regional_data (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  region_name TEXT NOT NULL,
  year INTEGER NOT NULL REFERENCES eras_master(year),
  topic TEXT NOT NULL,
  news_detail TEXT NOT NULL
);

-- life_simulations: シミュレーション結果
CREATE TABLE IF NOT EXISTS life_simulations (
  id UUID DEFAULT gen_random_uuid() PRIMARY KEY,
  created_at TIMESTAMPTZ DEFAULT now(),
  birth_year INTEGER NOT NULL,
  gender TEXT NOT NULL,
  region TEXT NOT NULL,
  result_data JSONB NOT NULL
);

-- Indexes
CREATE INDEX IF NOT EXISTS idx_social_events_year ON social_events(year);
CREATE INDEX IF NOT EXISTS idx_regional_data_year_region ON regional_data(year, region_name);
CREATE INDEX IF NOT EXISTS idx_life_simulations_created ON life_simulations(created_at DESC);

